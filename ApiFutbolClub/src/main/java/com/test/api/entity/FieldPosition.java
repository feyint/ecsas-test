package com.test.api.entity;

import java.io.Serializable;
import jakarta.persistence.Basic;
import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.NamedQueries;
import jakarta.persistence.NamedQuery;
import jakarta.persistence.Table;

/**
 *
 * @author feyin
 */
@Entity
@Table(name = "field_position")
@NamedQueries({
    @NamedQuery(name = "FieldPosition.findAll", query = "SELECT f FROM FieldPosition f"),
    @NamedQuery(name = "FieldPosition.findById", query = "SELECT f FROM FieldPosition f WHERE f.id = :id"),
    @NamedQuery(name = "FieldPosition.findByName", query = "SELECT f FROM FieldPosition f WHERE f.name = :name")})
public class FieldPosition implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Column(name = "name")
    private String name;
   

    public FieldPosition() {
    }

    public FieldPosition(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof FieldPosition)) {
            return false;
        }
        FieldPosition other = (FieldPosition) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.test.common.entity.FieldPosition[ id=" + id + " ]";
    }
    
}
